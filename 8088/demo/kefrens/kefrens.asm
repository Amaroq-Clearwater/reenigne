segment code

..start:        ; Needs to be at offset 0

  ; Determine if loader is present, and abort if not

  ; First, check to see if the API is even present.
  ; If not, don't try to call anything since it will hang the system.
  xor bx,bx
  mov es,bx
  mov di,(demoAPI * 4)+2      ;check to see if our INT is empty
  cmp [word es:di],bx         ;int. vector empty?
  je  exitShort               ;abort if so
  mov ax,0700h                ;check int. vector to see if it's ours
  int demoAPI
  cmp ax,0C0DEh               ;magic cookie received?
  jne exitShort               ;abort if not
  jmp mstart
exitShort:
  jmp exitEffect
mstart:

  mov sp,stackEnd

  mov ax,cs
  mov es,ax
  mov ds,ax

  mov di,unrolledCode

  ; Copy header

  mov cx,headerSize
  mov si,header
  cld
  rep movsb

  ; Unroll

  mov cx,scanlines
  mov bp,segTable
unroll:
  mov si,kefrensScanline
  movsb
  mov ax,bp
  stosw
  mov dx,cx
  mov cx,15
  rep movsw
  movsb
  mov cx,dx
  add bp,strideSegs     ; Increase raster segment
  loop unroll

  ; Copy footer

  mov cx,footerSize
  mov si,footer
  rep movsb


  ; Create pixel table

  mov ax,ss
  mov es,ax
  xor dx,dx
  xor di,di
pixelTableOuterLoop:

  mov cx,positions/2
  xor bx,bx
pixelTable3Loop:
  mov si,pixelTable3
  mov ax,bx
  stosw
  times 4 movsw          ; Even y, even x
  mov ax,dx
  or al,0x30
  stosw
  mov ax,bx
  stosw
  times 4 movsw          ; Even y, odd x
  mov ax,dx
  or al,0x30
  stosw
  inc bx
  loop pixelTable3Loop

  mov cx,positions/2
  mov bx,80
pixelTable1Loop:
  mov si,pixelTable1
  mov ax,bx
  stosw
  times 4 movsw          ; Odd y, even x
  mov ax,dx
  or al,0x10
  stosw
  mov ax,bx
  stosw
  times 4 movsw          ; Odd y, odd x
  mov ax,dx
  or al,0x10
  stosw
  inc bx
  loop pixelTable1Loop

  inc dx
  cmp dx,16
  jne pixelTableOuterLoop


  ; Create temporary xTables

  mov dx,segSamples
  mov es,dx
  mov ds,dx
  mov cx,154
  xor di,di
  xor ax,ax
tempXTable:
  stosw
  add ax,1848
  stosw
  add ax,12-1848
  add di,0x100-4
  loop tempXTable


  ; Create kefrens table

  mov dx,segTable
  mov si,yTable
initYLoop:
  db 0x2e,0xad ; cs:lodsw
  mov [cs:patch6+3],ax
  db 0x2e,0xad ; cs:lodsw
  mov [cs:patch7+3],ax
  db 0x2e,0xad ; cs:lodsw
  mov [cs:patch8+3],ax

  xor byte[cs:patch5+2],2

  push si

; CS
; DS = CS        // segSamples
; ES = segTable
; SS = segStack  // CS

  mov ax,cs
  mov si,frameTable
  mov cx,frames

  xor di,di
  mov es,dx

initFrameLoop:
  mov bp,[cs:si]
patch6:
  mov bx,[cs:bp+sintab_42]  ; bp = sintab_42[frameno_20020_acc + y_521]
  mov bp,[cs:si+2]
patch7:
  add bx,[cs:bp+sintab_25]  ; bp = sintab_42[frameno_20020_acc + y_521] + sintab_25[frameno_240247_acc + y_1043]
  mov bp,[cs:si+4]
patch8:
  add bx,[cs:bp+sintab_76]  ; bp = sintab_42[frameno_20020_acc + y_521] + sintab_25[frameno_240247_acc + y_1043] + sintab_76[frameno_140144_acc + y_642] = x
  add si,6
  mov bl,0
  mov ax,[es:di]      ; 3 2
patch5:
  add ax,[bx+2]       ; 2 2
  stosw               ; 1 2  12
  loop initFrameLoop

  pop si

  add dx,strideSegs
  cmp dx,segTable + strideSegs*scanlines
  jne initYLoop


  ; Ready to start; tell API we are waiting, then proceed when
  ; we get the signal.
  mov ah,01                   ;tell loader we are ready to start
  int demoAPI
  mov ah,02                   ;wait for signal to proceed
  stc
waitLoader:
  int demoAPI
  jnc waitLoader              ;if carry not set, don't start yet


  ; Go into lockstep and reduce DRAM refresh frequency to 4 refreshes per scanline

  lockstep2
  mov al,TIMER1 | LSB | MODE2 | BINARY
  out 0x43,al
  mov al,19
  out 0x41,al  ; Timer 1 rate


  ; Video layout:
  ;   We don't want to have to use the second 8kB so use Max Scan Line Address = 0
  ; scanlines 260..261 startup - set Vertical Total to 1
  ;             0..199 active video
  ;           200..201 set Vertical Total to 59              0.. 1
  ;           202..223 overscan bottom                       2..23
  ;           224..239 vertical sync                        24..39
  ;           240..259 overscan top                         40..59


  ; Set video mode to 2bpp

  mov dx,0x3d8
  mov al,0x0a
  out dx,al

  ; Palette
  ;      1 +OVERSCAN B
  ;      2 +OVERSCAN G
  ;      4 +OVERSCAN R
  ;      8 +OVERSCAN I
  ;   0x10 +BACKGROUND I
  ;   0x20 +COLOR SEL
  inc dx
  mov al,0x30
  out dx,al

  mov dl,0xd4

  ;   0xff Horizontal Total                             38
  mov ax,0x3800
  out dx,ax

  ;   0xff Horizontal Displayed                         28
  mov ax,0x2801
  out dx,ax

  ;   0xff Horizontal Sync Position                     2d
  mov ax,0x2d02
  out dx,ax

  ;   0x0f Horizontal Sync Width                        0a
  mov ax,0x0a03
  out dx,ax

  ;   0x7f Vertical Total                               01
  mov ax,0x0104
  out dx,ax

  ;   0x1f Vertical Total Adjust                        00
  mov ax,0x0005
  out dx,ax

  ;   0x7f Vertical Displayed                           02
  mov ax,0x0206
  out dx,ax

  ;   0x7f Vertical Sync Position                       18
  mov ax,0x1807
  out dx,ax

  ;   0x03 Interlace Mode                               02
  mov ax,0x0208
  out dx,ax

  ;   0x1f Max Scan Line Address                        00
  mov ax,0x0009
  out dx,ax

  ; Cursor Start                                        06
  ;   0x1f Cursor Start                                  6
  ;   0x60 Cursor Mode                                   0
  mov ax,0x060a
  out dx,ax

  ;   0x1f Cursor End                                   07
  mov ax,0x070b
  out dx,ax

  ;   0x3f Start Address (H)                            00
  mov ax,0x000c
  out dx,ax

  ;   0xff Start Address (L)                            00
  inc ax
  out dx,ax

  ;   0x3f Cursor (H)                                   03  0x3c0 == 40*24 == start of last line
  mov ax,0x030e
  out dx,ax

  ;   0xff Cursor (L)                                   c0
  mov ax,0xc00f
  out dx,ax

  times 38 nop


  ; Setup initial registers and clear screen

  xor ax,ax
  mov es,ax
  mov ax,[es:8*4]
  mov [cs:savedInt8],ax
  mov word[es:8*4],newInt8
  mov ax,[es:8*4+2]
  mov [cs:savedInt8+2],ax
  mov [es:8*4+2],cs
  writePIT16 0,2,19912


  mov ax,0xb800
  mov es,ax
  mov cx,80
  xor ax,ax
  xor di,di
  rep stosw
  mov dx,0x03d4
  mov bp,[sampleSeg]
  mov bx,0
  mov word[songPosition],songTable
  mov si,[songTable]


  ; Do the actual effect

  call unrolledCode


  ; Turn off speaker

;  in al,0x61
;  and al,0xfc
;  out 0x61,al


  ; Finish up

  xor ax,ax
  mov es,ax
  mov ax,[cs:savedInt8]
  mov [es:8*4],ax
  mov ax,[cs:savedInt8+2]
  mov [es:8*4+2],ax

;  refreshOn
;  mov ax,3
;  int 0x10
exitEffect:
  mov ax,0x4c00
  int 0x21

savedInt8: dw 0, 0

newInt8:
  mov al,0x20
  out 0x20,al
  iret


  ; This is the inner loop code. It's not called directory, but instead used
  ; as source data for the unroller, which creates 200 copies with the XXs
  ; filled in.

kefrensScanline:
  db 0xb8              ; mov ax,XXXX
  mov ds,ax            ; 2 0
  mov sp,[bx]          ; 2 2

  pop di               ; 1 2
  mov al,[es:di]       ; 3 1 +WS
  pop cx               ; 1 2
  and ax,cx            ; 2 0
  pop cx               ; 1 2
  or ax,cx             ; 2 0
  stosw                ; 1 2 +WS +WS
  pop ax               ; 1 2
  and ah,[es:di+1]     ; 4 1 +WS
  pop cx               ; 1 2
  or ax,cx             ; 2 0
  stosw                ; 1 2 +WS +WS

  pop ax               ; 1 2
  out dx,al            ; 1 1
  mov ds,bp            ; 2 0
  lodsb                ; 1 1
  out soundPort,al     ; 2 1        Total 1 (2) 31 = 34 bytes
kefrensScanlineEnd:

;       AND     OR      AND     OR       AND     OR      AND     OR        abcdefg =
;      cdab    cdab    g.ef    g.ef     bc.a    bc.a    fgde    fgde       6b9da75
